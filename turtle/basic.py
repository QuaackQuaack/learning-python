import turtle

#we need a window to draw and turtle to move
#let us denote window as wn and turtle as skk

wn = turtle.Screen() # we create a screen
wn.bgcolor("light green")
wn.title("Turtle")
skk = turtle.Turtle() # a turtle here 

skk.forward(100) #to move 100 pixel, toward the direction of skk facing
turtle.done()
